function bubbleInfo() {
  return `Bubble sort gets its name because smaller elements
  bubble toward the top of the list. This algorithm compares
  two adjacent values. If the first one is larger than the
  second, then it will swap those two values. After repeating
  this process all of the way through the data set, the largest
  value will have been continually swapped to the very end,
  arriving in it's final sorted position. Repeating this process
  the same number of times as there are values to be sorted
  will result in a sorted array.`;
}

module.exports = bubbleInfo;
